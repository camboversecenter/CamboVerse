/**
 * Generates src/cambodia-outline.ts from a real Cambodia border GeoJSON.
 *
 * Source: scripts/data/khm.geo.json (a low-resolution, 17-point national
 * border). Projects lng/lat equirectangularly (with a cos-latitude correction)
 * onto the map plane [x, z] (north is -z), fit and centered to a target size,
 * and bakes the same projection constants into a `projectLatLng` helper so the
 * heritage pins line up exactly with the outline.
 *
 * Run: `node scripts/generate-map-outline.mjs`
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(__dirname, "data/khm.geo.json");
const OUT = resolve(__dirname, "../src/cambodia-outline.ts");

// Fit target (world units on the map plane, before any group scaling).
const TARGET_W = 9;
const TARGET_H = 7.5;

const geo = JSON.parse(readFileSync(SRC, "utf8"));
const ring = geo.features[0].geometry.coordinates[0]; // [lng, lat][]

const lats = ring.map((p) => p[1]);
const lngs = ring.map((p) => p[0]);
const LAT_C = (Math.min(...lats) + Math.max(...lats)) / 2;
const LNG_C = (Math.min(...lngs) + Math.max(...lngs)) / 2;
const COS_LAT = Math.cos((LAT_C * Math.PI) / 180);

const raw = ring.map(([lng, lat]) => [(lng - LNG_C) * COS_LAT, -(lat - LAT_C)]);
const pxs = raw.map((p) => p[0]);
const pzs = raw.map((p) => p[1]);
const PX_MID = (Math.min(...pxs) + Math.max(...pxs)) / 2;
const PZ_MID = (Math.min(...pzs) + Math.max(...pzs)) / 2;
const SCALE = Math.min(
  TARGET_W / (Math.max(...pxs) - Math.min(...pxs)),
  TARGET_H / (Math.max(...pzs) - Math.min(...pzs)),
);

const r = (n) => Math.round(n * 1000) / 1000;
const outline = raw.map(([px, pz]) => [r((px - PX_MID) * SCALE), r((pz - PZ_MID) * SCALE)]);

const ts = `// AUTO-GENERATED by scripts/generate-map-outline.mjs from a real Cambodia
// border GeoJSON (low-resolution, 17-point). Do not hand-edit — run
// \`npm run gen:map\` to regenerate. Coordinates are [x, z] on the map plane
// (north is -z).

/** Simplified Cambodia border, projected to map-plane [x, z]. */
export const CAMBODIA_OUTLINE: [number, number][] = ${JSON.stringify(outline)};

// Projection constants baked from the same fit, so pins placed by lat/lng line
// up exactly with the outline above.
const LAT_C = ${LAT_C};
const LNG_C = ${LNG_C};
const COS_LAT = ${r(COS_LAT)};
const PX_MID = ${r(PX_MID)};
const PZ_MID = ${r(PZ_MID)};
const SCALE = ${r(SCALE)};

/** Project a real [lat, lng] onto the map plane as [x, z]. */
export function projectLatLng(lat: number, lng: number): [number, number] {
  const px = (lng - LNG_C) * COS_LAT;
  const pz = -(lat - LAT_C);
  return [(px - PX_MID) * SCALE, (pz - PZ_MID) * SCALE];
}
`;

writeFileSync(OUT, ts);
console.log(`wrote ${OUT} (${outline.length} points, scale=${r(SCALE)})`);
