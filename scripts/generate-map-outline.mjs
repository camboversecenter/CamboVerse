/**
 * Generates src/cambodia-outline.ts from a real Cambodia border GeoJSON.
 *
 * Source: scripts/data/khm.geo.json (a high-resolution national border, ~1000
 * points). The outer ring is simplified with Douglas–Peucker to a clean ~200
 * points, then projected equirectangularly (with a cos-latitude correction)
 * onto the map plane [x, z] (north is -z), fit and centered to a target size.
 * The same projection constants are baked into a `projectLatLng` helper so the
 * heritage pins line up exactly with the outline.
 *
 * Run: `node scripts/generate-map-outline.mjs`
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(__dirname, "data/khm.geo.json");
const OUT = resolve(__dirname, "../src/cambodia-outline.ts");

const TARGET_W = 9;
const TARGET_H = 7.5;
const EPSILON = 0.012; // simplification tolerance in degrees (~1.3 km)

// --- Douglas–Peucker on [lng, lat] points ---
function perpDist(p, a, b) {
  const dx = b[0] - a[0];
  const dy = b[1] - a[1];
  const l2 = dx * dx + dy * dy;
  if (l2 === 0) return Math.hypot(p[0] - a[0], p[1] - a[1]);
  let t = ((p[0] - a[0]) * dx + (p[1] - a[1]) * dy) / l2;
  t = Math.max(0, Math.min(1, t));
  return Math.hypot(p[0] - (a[0] + t * dx), p[1] - (a[1] + t * dy));
}
function dp(pts, eps) {
  if (pts.length < 3) return pts;
  let idx = -1;
  let dmax = 0;
  for (let i = 1; i < pts.length - 1; i++) {
    const d = perpDist(pts[i], pts[0], pts[pts.length - 1]);
    if (d > dmax) {
      dmax = d;
      idx = i;
    }
  }
  if (dmax > eps) {
    return dp(pts.slice(0, idx + 1), eps).slice(0, -1).concat(dp(pts.slice(idx), eps));
  }
  return [pts[0], pts[pts.length - 1]];
}

const geo = JSON.parse(readFileSync(SRC, "utf8"));
// Largest ring across Polygon / MultiPolygon.
const polys = geo.features[0].geometry.coordinates;
const rings = geo.features[0].geometry.type === "MultiPolygon" ? polys.map((p) => p[0]) : [polys[0]];
let ring = rings.sort((a, b) => b.length - a.length)[0];
// Drop duplicate closing vertex for simplification.
if (ring.length > 1 && ring[0][0] === ring[ring.length - 1][0] && ring[0][1] === ring[ring.length - 1][1]) {
  ring = ring.slice(0, -1);
}
ring = dp(ring, EPSILON);

const lats = ring.map((p) => p[1]);
const lngs = ring.map((p) => p[0]);
const LAT_C = (Math.min(...lats) + Math.max(...lats)) / 2;
const LNG_C = (Math.min(...lngs) + Math.max(...lngs)) / 2;
const COS_LAT = Math.cos((LAT_C * Math.PI) / 180);

const raw = ring.map(([lng, lat]) => [(lng - LNG_C) * COS_LAT, -(lat - LAT_C)]);
const pxs = raw.map((p) => p[0]);
const pzs = raw.map((p) => p[1]);
const PX_MID = (Math.min(...pxs) + Math.max(...pxs)) / 2;
const PZ_MID = (Math.min(...pzs) + Math.max(...pzs)) / 2;
const SCALE = Math.min(
  TARGET_W / (Math.max(...pxs) - Math.min(...pxs)),
  TARGET_H / (Math.max(...pzs) - Math.min(...pzs)),
);

const r = (n) => Math.round(n * 1000) / 1000;
const outline = raw.map(([px, pz]) => [r((px - PX_MID) * SCALE), r((pz - PZ_MID) * SCALE)]);

const ts = `// AUTO-GENERATED by scripts/generate-map-outline.mjs from a real Cambodia
// border GeoJSON (simplified with Douglas–Peucker). Do not hand-edit — run
// \`npm run gen:map\` to regenerate. Coordinates are [x, z] on the map plane
// (north is -z).

/** Simplified Cambodia border, projected to map-plane [x, z]. */
export const CAMBODIA_OUTLINE: [number, number][] = ${JSON.stringify(outline)};

// Projection constants baked from the same fit, so pins placed by lat/lng line
// up exactly with the outline above.
const LAT_C = ${LAT_C};
const LNG_C = ${LNG_C};
const COS_LAT = ${r(COS_LAT)};
const PX_MID = ${r(PX_MID)};
const PZ_MID = ${r(PZ_MID)};
const SCALE = ${r(SCALE)};

/** Project a real [lat, lng] onto the map plane as [x, z]. */
export function projectLatLng(lat: number, lng: number): [number, number] {
  const px = (lng - LNG_C) * COS_LAT;
  const pz = -(lat - LAT_C);
  return [(px - PX_MID) * SCALE, (pz - PZ_MID) * SCALE];
}
`;

writeFileSync(OUT, ts);
console.log(`wrote ${OUT} (${outline.length} points from ${rings[0].length} raw, scale=${r(SCALE)})`);
