/**
 * src/glyphPaths.ts — extract each Khmer letter's outline from the OFL fonts
 * (Noto Sans Khmer + Moul) into normalized SVG path data, so the Alphabet
 * Classroom can animate the letter being written (a pen tracing the outline).
 *
 * Build-time only: uses opentype.js (a devDependency) and the TTFs cached in
 * scripts/.fontcache/ (gitignored — fetch them once from the OFL sources):
 *   curl -sL -o scripts/.fontcache/Moul.ttf \
 *     https://raw.githubusercontent.com/google/fonts/main/ofl/moul/Moul-Regular.ttf
 *   curl -sL -o scripts/.fontcache/NotoSansKhmer.ttf \
 *     "https://raw.githubusercontent.com/google/fonts/main/ofl/notosanskhmer/NotoSansKhmer%5Bwdth%2Cwght%5D.ttf"
 *
 * Only single-codepoint letters get a path (multi-mark vowels are skipped).
 * Run: `node scripts/generate-glyph-paths.mjs`
 */
import opentype from "opentype.js";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { readFileSync, writeFileSync } from "node:fs";

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(HERE, "../src/glyphPaths.ts");
const CACHE = resolve(HERE, ".fontcache");

// Every letter char in src/khmer.ts (kept in sync by hand — build tooling only).
const CHARS = [
  // consonants
  "ក", "ខ", "គ", "ឃ", "ង", "ច", "ឆ", "ជ", "ឈ", "ញ", "ដ", "ឋ", "ឌ", "ឍ", "ណ",
  "ត", "ថ", "ទ", "ធ", "ន", "ប", "ផ", "ព", "ភ", "ម", "យ", "រ", "ល", "វ", "ស", "ហ", "ឡ", "អ",
  // dependent vowels (multi-codepoint ones are skipped automatically)
  "ា", "ិ", "ី", "ឹ", "ឺ", "ុ", "ូ", "ួ", "ើ", "ឿ", "ៀ", "េ", "ែ", "ៃ", "ោ", "ៅ", "ុំ", "ំ", "ាំ", "ះ",
  // independent vowels
  "ឥ", "ឦ", "ឧ", "ឩ", "ឪ", "ឫ", "ឬ", "ឭ", "ឮ", "ឯ", "ឰ", "ឱ", "ឳ",
  // numerals
  "០", "១", "២", "៣", "៤", "៥", "៦", "៧", "៨", "៩",
];

const FONTS = {
  normal: opentype.parse(readFileSync(resolve(CACHE, "NotoSansKhmer.ttf")).buffer.slice(0)),
  moul: opentype.parse(readFileSync(resolve(CACHE, "Moul.ttf")).buffer.slice(0)),
};

const r = (v) => Math.round(v * 10) / 10;

/** Get a glyph outline, normalized+centred into a 0..100 box (SVG y-down). */
function pathFor(font, ch) {
  const p = font.getPath(ch, 0, 0, 1000);
  if (!p.commands.length) return null;
  const bb = p.getBoundingBox();
  const w = bb.x2 - bb.x1;
  const h = bb.y2 - bb.y1;
  if (w <= 0 || h <= 0) return null;
  const s = 82 / Math.max(w, h);
  const cx = (bb.x1 + bb.x2) / 2;
  const cy = (bb.y1 + bb.y2) / 2;
  const X = (x) => r((x - cx) * s + 50);
  const Y = (y) => r((y - cy) * s + 50);
  let d = "";
  for (const c of p.commands) {
    if (c.type === "M") d += `M${X(c.x)} ${Y(c.y)}`;
    else if (c.type === "L") d += `L${X(c.x)} ${Y(c.y)}`;
    else if (c.type === "C") d += `C${X(c.x1)} ${Y(c.y1)} ${X(c.x2)} ${Y(c.y2)} ${X(c.x)} ${Y(c.y)}`;
    else if (c.type === "Q") d += `Q${X(c.x1)} ${Y(c.y1)} ${X(c.x)} ${Y(c.y)}`;
    else if (c.type === "Z") d += "Z";
  }
  return d;
}

const out = {};
let n = 0;
let skipped = 0;
for (const ch of CHARS) {
  if ([...ch].length !== 1) {
    skipped++;
    continue; // multi-codepoint (combining) — needs shaping; skip
  }
  const normal = pathFor(FONTS.normal, ch);
  const moul = pathFor(FONTS.moul, ch);
  if (normal && moul) {
    out[ch] = { normal, moul };
    n++;
  } else {
    skipped++;
  }
}

const body =
  `/**\n` +
  ` * Normalized SVG outline paths (0..100 box) for each Khmer letter, in the\n` +
  ` * Normal (Noto Sans Khmer) and Moul shapes. Generated by\n` +
  ` * scripts/generate-glyph-paths.mjs from the OFL fonts — do not edit by hand.\n` +
  ` * Used by the Alphabet Classroom to animate writing each letter.\n` +
  ` */\n` +
  `export interface GlyphPath { normal: string; moul: string }\n\n` +
  `export const GLYPH_PATHS: Record<string, GlyphPath> = ${JSON.stringify(out, null, 0)};\n`;

writeFileSync(OUT, body);
console.log(`wrote ${OUT} — ${n} letters, ${skipped} skipped, ${(body.length / 1024).toFixed(1)} KB`);
