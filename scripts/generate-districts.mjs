/**
 * Generates src/cambodia-districts.ts from the official ADM2 district boundary
 * GeoJSON (scripts/data/cambodia-districts.geojson, khm_admbnda_adm2 — 197
 * districts).
 *
 * Each district polygon ring is simplified with Douglas–Peucker and projected
 * to the map plane [x, z] using the SAME baked constants as
 * src/cambodia-outline.ts, so districts line up exactly with the province
 * borders, the national outline, and the heritage pins. Districts carry their
 * parent province's ADM1 pcode so the province map can render just its own.
 *
 * Run: `node scripts/generate-districts.mjs`  (or `npm run gen:districts`)
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(__dirname, "data/cambodia-districts.geojson");
const OUT = resolve(__dirname, "../src/cambodia-districts.ts");

// Must match src/cambodia-outline.ts exactly (that file's `projectLatLng`).
const LAT_C = 12.547998784089925;
const LNG_C = 104.98054504394531;
const COS_LAT = 0.976;
const SCALE = 1.742;

const EPSILON = 0.007; // simplify tolerance in degrees (~0.8 km) — finer than ADM1
const MIN_POINTS = 4;
const MIN_SPAN = 0.03; // drop slivers/tiny islands

// --- Douglas–Peucker on [lng, lat] points ---
function perpDist(p, a, b) {
  const dx = b[0] - a[0];
  const dy = b[1] - a[1];
  const l2 = dx * dx + dy * dy;
  if (l2 === 0) return Math.hypot(p[0] - a[0], p[1] - a[1]);
  let t = ((p[0] - a[0]) * dx + (p[1] - a[1]) * dy) / l2;
  t = Math.max(0, Math.min(1, t));
  return Math.hypot(p[0] - (a[0] + t * dx), p[1] - (a[1] + t * dy));
}
function dp(pts, eps) {
  if (pts.length < 3) return pts;
  let idx = -1;
  let dmax = 0;
  for (let i = 1; i < pts.length - 1; i++) {
    const d = perpDist(pts[i], pts[0], pts[pts.length - 1]);
    if (d > dmax) {
      dmax = d;
      idx = i;
    }
  }
  if (dmax > eps) {
    return dp(pts.slice(0, idx + 1), eps).slice(0, -1).concat(dp(pts.slice(idx), eps));
  }
  return [pts[0], pts[pts.length - 1]];
}

const project = ([lng, lat]) => [(lng - LNG_C) * COS_LAT * SCALE, -(lat - LAT_C) * SCALE];
const r = (n) => Math.round(n * 1000) / 1000;

const geo = JSON.parse(readFileSync(SRC, "utf8"));
let totalPts = 0;

const districts = geo.features.map((f) => {
  const polys = f.geometry.type === "MultiPolygon" ? f.geometry.coordinates : [f.geometry.coordinates];
  const rings = [];
  for (const poly of polys) {
    let ring = poly[0]; // outer ring of each polygon
    if (
      ring.length > 1 &&
      ring[0][0] === ring[ring.length - 1][0] &&
      ring[0][1] === ring[ring.length - 1][1]
    ) {
      ring = ring.slice(0, -1);
    }
    const simple = dp(ring, EPSILON);
    if (simple.length < MIN_POINTS) continue;
    const proj = simple.map(project);
    const xs = proj.map((p) => p[0]);
    const zs = proj.map((p) => p[1]);
    if (Math.max(...xs) - Math.min(...xs) < MIN_SPAN && Math.max(...zs) - Math.min(...zs) < MIN_SPAN) {
      continue;
    }
    rings.push(proj.map(([x, z]) => [r(x), r(z)]));
    totalPts += proj.length;
  }
  return {
    name: f.properties.ADM2_EN,
    pcode: f.properties.ADM2_PCODE,
    province: f.properties.ADM1_PCODE, // parent ADM1 pcode
    rings,
  };
}).filter((d) => d.rings.length > 0);

const ts = `// AUTO-GENERATED by scripts/generate-districts.mjs from the official ADM2
// district boundary GeoJSON (khm_admbnda_adm2, 197 districts), simplified with
// Douglas–Peucker and projected with the SAME constants as cambodia-outline.ts.
// Do not hand-edit — run \`npm run gen:districts\`. Each ring is a closed
// boundary polyline in map-plane [x, z] (north is -z). \`province\` is the parent
// ADM1 pcode (matches Province.pcode in cambodia-provinces.ts).

export interface District {
  name: string;
  pcode: string;
  province: string;
  rings: [number, number][][];
}

export const CAMBODIA_DISTRICTS: District[] = ${JSON.stringify(districts)};
`;

writeFileSync(OUT, ts);
console.log(
  `wrote ${OUT} (${districts.length} districts, ${districts.reduce((n, d) => n + d.rings.length, 0)} rings, ${totalPts} points)`,
);
