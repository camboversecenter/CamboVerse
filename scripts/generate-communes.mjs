/**
 * Generates the per-province commune (ADM3) data under src/communes/<ADM1>.ts
 * from the official ADM3 boundary GeoJSON (scripts/data/cambodia-communes.geojson,
 * khm_admbnda_adm3 — 1,633 communes).
 *
 * Communes are the fourth map tier. There are far too many to ship in one
 * bundle, so we split them **by province**: each `src/communes/<ADM1_PCODE>.ts`
 * holds only that province's communes, and the app lazy-loads just the one it
 * needs (see src/communes.ts). Every ring is simplified with Douglas–Peucker
 * and projected with the SAME constants as cambodia-outline.ts, so communes line
 * up exactly with the district, province, and national borders.
 *
 * Run: `node scripts/generate-communes.mjs`  (or `npm run gen:communes`)
 */
import { readFileSync, writeFileSync, readdirSync, unlinkSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(__dirname, "data/cambodia-communes.geojson");
const OUT_DIR = resolve(__dirname, "../src/communes");

// Must match src/cambodia-outline.ts exactly (that file's `projectLatLng`).
const LAT_C = 12.547998784089925;
const LNG_C = 104.98054504394531;
const COS_LAT = 0.976;
const SCALE = 1.742;

const EPSILON = 0.005; // ~0.55 km — fine enough for small communes, still compact
const MIN_POINTS = 4;
const MIN_SPAN = 0.02;

function perpDist(p, a, b) {
  const dx = b[0] - a[0], dy = b[1] - a[1];
  const l2 = dx * dx + dy * dy;
  if (l2 === 0) return Math.hypot(p[0] - a[0], p[1] - a[1]);
  let t = ((p[0] - a[0]) * dx + (p[1] - a[1]) * dy) / l2;
  t = Math.max(0, Math.min(1, t));
  return Math.hypot(p[0] - (a[0] + t * dx), p[1] - (a[1] + t * dy));
}
function dp(pts, eps) {
  if (pts.length < 3) return pts;
  let idx = -1, dmax = 0;
  for (let i = 1; i < pts.length - 1; i++) {
    const d = perpDist(pts[i], pts[0], pts[pts.length - 1]);
    if (d > dmax) { dmax = d; idx = i; }
  }
  if (dmax > eps) return dp(pts.slice(0, idx + 1), eps).slice(0, -1).concat(dp(pts.slice(idx), eps));
  return [pts[0], pts[pts.length - 1]];
}
const project = ([lng, lat]) => [(lng - LNG_C) * COS_LAT * SCALE, -(lat - LAT_C) * SCALE];
const r = (n) => Math.round(n * 1000) / 1000;

const geo = JSON.parse(readFileSync(SRC, "utf8"));
const byProvince = new Map();
let totalPts = 0, kept = 0;

for (const f of geo.features) {
  const polys = f.geometry.type === "MultiPolygon" ? f.geometry.coordinates : [f.geometry.coordinates];
  const rings = [];
  for (const poly of polys) {
    let ring = poly[0];
    if (ring.length > 1 && ring[0][0] === ring[ring.length - 1][0] && ring[0][1] === ring[ring.length - 1][1]) {
      ring = ring.slice(0, -1);
    }
    const simple = dp(ring, EPSILON);
    if (simple.length < MIN_POINTS) continue;
    const proj = simple.map(project);
    const xs = proj.map((p) => p[0]), zs = proj.map((p) => p[1]);
    if (Math.max(...xs) - Math.min(...xs) < MIN_SPAN && Math.max(...zs) - Math.min(...zs) < MIN_SPAN) continue;
    rings.push(proj.map(([x, z]) => [r(x), r(z)]));
    totalPts += proj.length;
  }
  if (rings.length === 0) continue;
  const prov = f.properties.ADM1_PCODE;
  if (!byProvince.has(prov)) byProvince.set(prov, []);
  byProvince.get(prov).push({
    name: f.properties.ADM3_EN,
    pcode: f.properties.ADM3_PCODE,
    district: f.properties.ADM2_PCODE,
    rings,
  });
  kept++;
}

// Clean out any stale per-province files, then write fresh ones.
for (const file of readdirSync(OUT_DIR)) {
  if (/^KH\d+\.ts$/.test(file)) unlinkSync(resolve(OUT_DIR, file));
}
for (const [prov, communes] of byProvince) {
  const ts = `// AUTO-GENERATED by scripts/generate-communes.mjs — do not hand-edit.
// Communes (ADM3) of province ${prov}, from khm_admbnda_adm3, simplified and
// projected with the same constants as cambodia-outline.ts. Loaded on demand
// via src/communes.ts. Ring points are map-plane [x, z] (north is -z).
export const COMMUNES = ${JSON.stringify(communes)};
`;
  writeFileSync(resolve(OUT_DIR, `${prov}.ts`), ts);
}

console.log(`wrote ${byProvince.size} province files under src/communes/ (${kept} communes, ${totalPts} points)`);
